"""
This module provides an addition operator
"""


class Adder:
    """
    Returns sum of two numbers.
    """

    @staticmethod
    def calc(operand_1, operand_2):
        """
        Adds two numbers.
        :param operand_1: number
        :param operand_2: number
        :return: Sum of two numbers
        """

        return operand_1 + operand_2
