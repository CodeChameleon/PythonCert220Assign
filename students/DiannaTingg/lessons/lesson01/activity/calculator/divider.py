"""
This module provides a division operator
"""


class Divider:
    """
    Returns quotient of two numbers.
    """

    @staticmethod
    def calc(operand_1, operand_2):
        """
        Divides two numbers
        :param operand_1: number
        :param operand_2: number
        :return: Division of two numbers
        """
        return operand_1/operand_2
